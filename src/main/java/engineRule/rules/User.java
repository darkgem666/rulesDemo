package engineRule.rules;

public class User {
	String jc;
	String c;
	String cc;
	String uid;
	public User(String jc, String c, String cc, String uid) {
		super();
		this.jc = jc;
		this.c = c;
		this.cc = cc;
		this.uid = uid;
	}
	public String getJc() {
		return jc;
	}
	public void setJc(String jc) {
		this.jc = jc;
	}
	public String getC() {
		return c;
	}
	public void setC(String c) {
		this.c = c;
	}
	public String getCc() {
		return cc;
	}
	public void setCc(String cc) {
		this.cc = cc;
	}
	public String getUid() {
		return uid;
	}
	public void setUid(String uid) {
		this.uid = uid;
	}
	
	
	
}
